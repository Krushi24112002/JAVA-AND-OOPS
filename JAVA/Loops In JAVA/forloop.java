// For Loop
/*

Syntax :

for (initialization Expression; test Expression; update Expression)
{
     // body of the loop statements we want to execute
}

 */

public class forloop {

  public static void main(String[] args) {
    for (int i = 1; i <= 5; i++) {
      System.out.println("KRUSHI MONPARA");
    }
  }
}
