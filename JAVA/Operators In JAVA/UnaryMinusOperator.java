// Unary Miuns(-) Operator
// This operator can be used to convert a positive value to a negative one.

public class UnaryMinusOperator {

  public static void main(String[] args) {
    int n1 = 24;
    System.out.println("Your Numbers Is " + n1);

    // Performing unary operation
    n1 = -n1;
    System.out.println("Your Number Converts In Unary Minus " + n1);
  }
}
