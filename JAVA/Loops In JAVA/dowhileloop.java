// Do While Loop
/*

-> do-while loop is an Exit control loop.

Syntax :

do
{
    // Loop Body
    Update_expression
}

// Condition check
while (test_expression);


 */

public class dowhileloop {

    public static void main(String[] args) {

        // initialization expression
        int i = 1;

        do {
            System.out.println("KRUSHI MONPARA");

            // update expression
            i++;

        } while (i < 5); // test expression
    }
}